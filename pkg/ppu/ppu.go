type PPU struct {
	// Registers []byte // 0x2000~0x2007
	MemoryMap [0x3FFF]byte
	Cycle     int
	Line      int
	charROM   []byte
}

func (p *PPU) Run(cycle int) {
	p.Cycle += cycle

	if p.Cycle >= 341 {
		p.Cycle -= 341
		p.Line++

		if p.Line <= 240 && p.Line%8 == 0 {
			//
		}
		if p.Line == 262 {
			p.Line = 0
		}
	}
}

func (p *PPU) buildSprite(spriteNum int) [][]byte {
	sprite := make([][]byte, 8)
	for i := 0; i < 8; i++ {
		sprite[i] = make([]byte, 8)
	}

	for i := 0; i < 16; i++ {
		for j := 0; j < 8; j++ {
			if p.charROM[spriteNum*16+i]&(0x80>>j) != 0 {
				sprite[i%8][j] += 0x01 << (i / 8)
			}
		}
	}
	return sprite
}

